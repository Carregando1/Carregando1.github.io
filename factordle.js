function euclid(a, b) {
  if (a == b) {
    throw new Error("Impossible");
  }
  if (a % b == 0 || b == 1) {
    return ['rgb(0,175,0)', '2'];
  }
  if (b == 0) {
    return ['rgb(150,150,150)', '0'];
  }
  while (a != 0 && b != 0) {
    if (a < b) {
      b = Math.max(a, b) % Math.min(a, b);
    } else {
      a = Math.max(a, b) % Math.min(a, b);
    }
  }
  if (a + b == 1) {
    return ['rgb(150,150,150)', '0'];
  }
  return ['rgb(200,200,0)', '1'];
}
